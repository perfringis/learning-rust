fn main() {
    // addition
    let sum = 5 + 10;

    // subtraction
    let difference = 95.5 - 4.3;

    // multiplication
    let product = 4 * 30;

    // division
    let quotient = 56.7 / 32.2;
    let truncated = -5 / 3;

    // reminder
    let remainder = 43 % 5;

    println!("value {sum}");
    println!("value {difference}");
    println!("value {product}");
    println!("value {quotient}");
    println!("value {truncated}");
    println!("value {remainder}");
}
